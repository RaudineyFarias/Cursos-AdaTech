public class manipulacao_string {

    public static void main(String[] args) {

        String nome = "Jessé";
        System.out.println(nome.toUpperCase());//Essa opção o java vai imprimir em Letra Maiúsculas
        System.out.println(nome.toLowerCase());//Essa opção o java vai imprimir em Letra Minusculas
        System.out.println(nome.length());//Essa opção o java vai imprimir o tamanho do texto
    }

}
